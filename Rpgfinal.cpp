class charc;
class player;
class interest;

class charc {
private:
    string name;
public:
    charc() {}
    charc(string n) {
        name = n;
    }
    virtual void display() {
        cout << name << endl;
    }
};

class interest : public charc {
private:
    int rizzed = 0;
public:
    interest(string n) : charc(n) {}
    void disprizzed() {
        cout << "You have rizzed: " << rizzed << endl;
    }
    int getrizzed(){
        return rizzed;
    }
    void display(){
        cout<< "Interest Name: ";
        charc::display();
    }

    void rizzup(int x, player &temp);
    void rizzdown(int x, player &temp);
};

class player : public charc {
private:
    int rizz;
public:
    player(string n) : charc(n) {
        rizz = 50;
    }
    int getrizz(){
        return rizz;
    }
    void disprizz() {
        cout << "Your rizz is: " << rizz << endl;
    }
    void display(){
        cout<<"Player Name: ";
        charc::display();
    }
    friend class interest;
};

void interest::rizzup(int x, player &temp) {
    rizzed += x;
    temp.rizz -=x;
}

void interest::rizzdown(int x, player &temp) {
    rizzed -= x;
    temp.rizz -=x;
}

